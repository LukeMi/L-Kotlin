package jeferry.kotlin.toturial.operater

/**
 * 算术运算符
 */
var a = 5
val b = 2
fun main(args: Array<String>) {
    println("算术运算符")
    println(" + - 加法\n - - 减法\n * - 乘法 \n / - 取整 \n  % - 取余 \n ++ - 自增\n -- - 自减")
    println("a = $a")
    println("b = $b")
    println("a + b = ${a + b}")
    println("a - b = ${a - b}")
    println("a * b = ${a * b}")
    println("a / b = ${a / b}")
    println("a % b = ${a % b}")
    println("++a = ${++a}")
    println("--a = ${--a}")
    println("-a = ${-a}")
}