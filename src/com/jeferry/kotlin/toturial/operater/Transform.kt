/**
 * 转换操作
 */
fun main() {
    println("transform operator - 显示转换")
    println(".toByte()")
    println(".toShort()")
    println(".toInt()")
    println(".toLong()")
    println(".toFloat()")
    println(".toDouble()")
    println(".toChar()")

    println()
    val a = 'a'
    println("a.toByte() = ${a.toByte()}")
    println("a.toShort() : ${a.toShort()}")
    println("a.toInt() = ${a.toInt()}")
    println("a.toLong() = ${a.toLong()}")
    println("a.toFloat() = ${a.toFloat()}")
    println("a.toDouble() = ${a.toDouble()}")
    println("a.toChar() = ${a.toChar()}")
}