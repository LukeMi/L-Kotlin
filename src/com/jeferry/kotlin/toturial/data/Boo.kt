package jeferry.kotlin.toturial.data

val aBoo: Boolean = true
val bBoo: Boolean = false

fun main() {
    println("aBoo = $aBoo")
    println("bBoo = $bBoo")
    println("aBoo == bBoo ? ${aBoo == bBoo}")
    println("aBoo != bBoo ? ${aBoo != bBoo}")
    println("aBoo && bBoo ? ${aBoo && bBoo}")
    println("aBoo || bBoo ? ${aBoo || bBoo}")
    println("aBoo xor bBoo ? ${aBoo xor bBoo}")
    println("!aBoo   ? ${!aBoo}")
}